import pandas as pd
data = pd.read_csv("/content/drive/My Drive/9Sem/20XT96 MS Lab/Coimbatore,India 2023-01-01 to 2023-12-31.csv")
data.head()

# Convert 'datetime' column to datetime format and extract day of year
